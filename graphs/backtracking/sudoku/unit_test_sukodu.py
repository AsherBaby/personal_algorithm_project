import unittest
from sudoku import Sudoku

class TestSolveSudoku(unittest.TestCase):

    def test_of_easy_board(self):
        board = [
            [4, 0, 0, 6, 0, 2, 0, 8, 1,],
            [2, 5, 0, 0, 8, 7, 0, 0, 0,],
            [0, 0, 6, 0, 0, 0, 2, 4, 0,],
            [0, 0, 0, 0, 0, 0, 3, 0, 0,],
            [0, 2, 7, 3, 5, 4, 8, 9, 0,],
            [0, 0, 5, 0, 0, 0, 0, 0, 0,],
            [0, 8, 2, 1, 0, 0, 4, 0, 0,],
            [0, 0, 0, 2, 7, 0, 0, 1, 8,],
            [6, 1, 0, 8, 0, 9, 0, 0, 5,]]
        s = Sudoku(board)
        s.solve()
        self.assertEqual(
            s.grid,
            [[4, 7, 9, 6, 3, 2, 5, 8, 1],
             [2, 5, 1, 4, 8, 7, 9, 6, 3],
             [8, 3, 6, 5, 9, 1, 2, 4, 7],
             [9, 4, 8, 7, 1, 6, 3, 5, 2],
             [1, 2, 7, 3, 5, 4, 8, 9, 6],
             [3, 6, 5, 9, 2, 8, 1, 7, 4],
             [7, 8, 2, 1, 6, 5, 4, 3, 9],
             [5, 9, 4, 2, 7, 3, 6, 1, 8],
             [6, 1, 3, 8, 4, 9, 7, 2, 5]])


if __name__ == '__main__':
    unittest.main()
